package com.syntax.class07;

public class DoLoop {

	public static void main(String[] args) {
		
		int num=1;
		
		while(num<=5) {
			System.out.println("Hello");
			num++;
		}
		System.out.println("-------------");
		
      int num1=1;
      do {
    	  System.out.println("Hello");
    	  num1++;
      }while(num<=5);
      
      System.out.println("-----");
      
      int e=1;
		do {
			System.out.println(e);
			e++;
		}while(e<=30);
      
	}

}
