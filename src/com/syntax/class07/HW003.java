public class HW003 {

	public static void main(String[] args) {
		//Task1
		System.out.println("Please print from 1 to 100");
		for (int y=1; y<=100; y++) {
			System.out.print(y+" ");
		}
      
		System.out.println("------------------");
		
		//Task2
		System.out.println("Please print from 100 to 1");
		for (int k=1; k<=100; k--) {
			System.out.print(k+" ");
	}

}
}